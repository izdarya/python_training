class contact:
    def __init__(self, first, middle, last, nickname, company, title, address, telephone_home, telephone_mobile,
                    work, email, year, address_s, home, notes, day, month):
        self.first = first
        self.middle = middle
        self.last = last
        self.nickname = nickname
        self.company = company
        self.title = title
        self.address = address
        self.telephone_home = telephone_home
        self.telephone_mobile = telephone_mobile
        self.work = work
        self.email = email
        self.year = year
        self.address_s = address_s
        self.home = home
        self.notes = notes
        self.day = day
        self.month = month


